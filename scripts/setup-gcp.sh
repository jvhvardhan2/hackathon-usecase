#!/bin/bash

# Healthcare Application - GCP Setup Script
# This script automates the initial setup of GCP infrastructure

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

print_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

print_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

check_prerequisites() {
    print_info "Checking prerequisites..."
    
    if ! command -v gcloud &> /dev/null; then
        print_error "gcloud CLI is not installed. Please install it first."
        exit 1
    fi
    
    if ! command -v terraform &> /dev/null; then
        print_error "Terraform is not installed. Please install it first."
        exit 1
    fi
    
    print_info "All prerequisites are installed âœ“"
}

echo "=========================================="
echo "Healthcare Application - GCP Setup"
echo "=========================================="

check_prerequisites

print_info "Please refer to SETUP.md for detailed setup instructions"
print_info "This is an automated setup script for GCP infrastructure"
